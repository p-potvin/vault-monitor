const LangContext = createContext<[Lang, (lang: Lang) => void] | null>(null);

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => localStorage.getItem('vault-monitor-locale') === 'qc' ? 'qc' : 'en');
  useEffect(() => {
    localStorage.setItem('vault-monitor-locale', lang);
    document.documentElement.lang = lang === 'qc' ? 'fr-CA' : 'en';
  }, [lang]);
  const value = useMemo<[Lang, (lang: Lang) => void]>(() => [lang, setLang], [lang]);
  return <LangContext.Provider value={value}>{children}</LangContext.Provider>;
}

export function useLangState() {
  const value = useContext(LangContext);
  if (!value) throw new Error('useLangState must be used inside LangProvider');
  return value;
}
